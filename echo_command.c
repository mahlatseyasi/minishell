#include "minishell.h"

void	echo_command(char *par[])
{
	int i;

	i = 1;
	if (par[1][0] == '$')
		printf("%s\n", getenv(strtok(par[1], "$")));
	else
	{
		while (par[i])
			printf("%s ", par[i++]);
		printf("\n");
	}
}
